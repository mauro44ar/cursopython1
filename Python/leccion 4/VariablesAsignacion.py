#variable de asignacion

variable=int(input('ingrese un valor numerico: '))

print (variable)

variable=variable+1 #esto es lo mismo que hacer variable+=1 hace que el valor de la variable anterior se le agregue 1

print(variable)

#se lo conoce como incremento de reasignacion

variable+=1

print (variable)

#decremento de reasignacion

variable -= 2

print(variable)

#multiplicacion de reasignacion

variable*=3

print(variable)

#division de reasignacion

variable /= 3

print (variable)

variable //= 3

print (variable)